async def show_levels(message: types.Message, subject: str):
    levels = [
        types.InlineKeyboardButton(text="Початковий рівень", callback_data=f"level_starting_{subject}"),
        types.InlineKeyboardButton(text="Середній рівень", callback_data=f"level_medium_{subject}"),
        types.InlineKeyboardButton(text="Достатній рівень", callback_data=f"level_sufficient_{subject}"),
        types.InlineKeyboardButton(text="Високий рівень", callback_data=f"level_high_{subject}")
    ]
    markup = types.InlineKeyboardMarkup(inline_keyboard=[levels])
    await message.answer("Виберіть рівень складності", reply_markup=markup)
